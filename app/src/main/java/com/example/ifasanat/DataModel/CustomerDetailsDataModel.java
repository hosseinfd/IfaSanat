package com.example.ifasanat.DataModel;

public class CustomerDetailsDataModel {
    private int id;
    private String utmx;
    private String utmy;
    private String twelveCode;
    private String province;
    private String city;
    private String village;
    private String cityPart;
    private String state;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUtmx() {
        return utmx;
    }

    public void setUtmx(String utmx) {
        this.utmx = utmx;
    }

    public String getUtmy() {
        return utmy;
    }

    public void setUtmy(String utmy) {
        this.utmy = utmy;
    }

    public String getTwelveCode() {
        return twelveCode;
    }

    public void setTwelveCode(String twelveCode) {
        this.twelveCode = twelveCode;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getVillage() {
        return village;
    }

    public void setVillage(String village) {
        this.village = village;
    }

    public String getCityPart() {
        return cityPart;
    }

    public void setCityPart(String cityPart) {
        this.cityPart = cityPart;
    }


    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
